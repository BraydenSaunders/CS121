package LoopsActivities;

public class forLoopPractice {
    public static void main(String[] args) {
        int numbers [] = {1,2,3,4,5};
        for (int num = 0; num < numbers.length; num++)
            System.out.println(numbers[num]);
        for (int i = 1; i<= 10; i++)
            System.out.println();
    }
}
